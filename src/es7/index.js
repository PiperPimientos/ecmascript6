//Veremos el Includes

//Crearemos un array que contenga numeros hata el 7

let numbers = [1, 2, 3, 7, 8]

// ahora, para utilizar el includes, tenemos que validar con un if y la siguiente sintaxis
//Validaremos que el array contenga el 7
if (numbers.includes(7)) {
    console.log('Si se encuentra el valor 7');
} else {
    console.log ('No se encuentra')
}

